// Type Conversion (or type casting) is the explicit conversion of a value from one type to another.

// Examples of Type Conversion
// 1. Converting to a Number:
let str = "42";
let num = Number(str);  // Converts string to number
console.log(num);        // Output: 42

// Converting to a String:
let num2 = 42;
let str2 = String(num2);   // Converts number to string
console.log(str2);        // Output: "42"

// Parseint and Parsefloat
let strInt = "42px";
let intNum = parseInt(strInt); // Converts string to integer
console.log(intNum);            // Output: 42

let strFloat = "42.5px";
let floatNum = parseFloat(strFloat); // Converts string to float
console.log(floatNum);


